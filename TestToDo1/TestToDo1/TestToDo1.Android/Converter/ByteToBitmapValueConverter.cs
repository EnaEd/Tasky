using Android.Graphics;
using MvvmCross.Platform.Converters;
using System;
using System.Globalization;

namespace TestToDo1.Core.Converters
{
    public class ByteToBitmapValueConverter : MvxValueConverter<byte[], Object>
    {
        protected override Object Convert(byte[] value, Type targetType, object parameter, CultureInfo culture)
        {
            if (value == null)
            {
                return null;
            }
            Bitmap img = Android.Graphics.BitmapFactory.DecodeByteArray(value, 0, value.Length);

            return img;
        }
    }
}
